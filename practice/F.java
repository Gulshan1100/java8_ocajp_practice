class F{
	public static void main(String args[]){
		int a=5;
		int b=3;
		int c=0;
		c= a+b;		// Arithmetic operators
		System.out.println(c);
		c=a-b;
		System.out.println(c);
		c=a*b;
		System.out.println(c);
		c=a/b;
		System.out.println(c);
		c=a%b;
		System.out.println(c);
		b=a++;
		System.out.println(a);
		System.out.println(b);
		b=++a;
		System.out.println(a);
		System.out.println(b);

		
	}
}
